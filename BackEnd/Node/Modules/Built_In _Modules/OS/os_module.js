
// Example using the 'os' module
import os from 'os'

console.log('Platform:', os.platform()); // Returns the operating system platform
console.log('CPU Architecture:', os.arch()); // Returns the CPU architecture
console.log('Total Memory (in bytes):', os.totalmem()); // Returns total system memory
console.log('Free Memory (in bytes):', os.freemem()); // Returns free system memory
